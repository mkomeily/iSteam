double Region_3_f_RhoT(double Rho,double T){
    double Delta = Rho / Region_3_f_RhoT_Rho_Star;
    double Tau = Region_3_f_RhoT_T_Star / T;
    return Rho * R  * T * Phi(Delta,Tau);
}
double Region_3_p_RhoT(double Rho, double T){
    double Delta = Rho / Region_3_f_RhoT_Rho_Star;
    double Tau = Region_3_f_RhoT_T_Star / T;
    return Rho * R  * T * Delta * PhiDelta(Delta,Tau) * 1000;
}
double Region_3_s_RhoT(double Rho, double T){
    double Delta = Rho / Region_3_f_RhoT_Rho_Star;
    double Tau = Region_3_f_RhoT_T_Star / T;
    return R * (Tau * PhiTau(Delta,Tau) - Phi(Delta,Tau));
}
double Region_3_h_RhoT(double Rho, double T){
    double Delta = Rho / Region_3_f_RhoT_Rho_Star;
    double Tau = Region_3_f_RhoT_T_Star / T;
    return R * T * (Tau * PhiTau(Delta,Tau) + Delta * PhiDelta(Delta,Tau));
}

